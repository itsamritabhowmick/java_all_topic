public class DataTypes {
    public static void main(String[] args) {
        int a;
        a = 45;
        System.out.println(a + " is Integer type of data");

        float b = 45.62f;
        System.out.println("Floating Number is :" + b);

        double d = 89.2364512;
        System.out.println("Double number is: " + d);

        boolean isTrue = true;
        System.out.println("It is :" + isTrue);

        String s = "hello";
        System.out.println(s + " is the string");

        char c = 'a';
        System.out.println(c + " Is the character type");
    }
}
