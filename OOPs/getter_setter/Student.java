public class Student {

    // attributes
    private String name;
    private int age;

    // using setter method
    public void setName(String n) {
        this.name = n;
    }

    // using getter method
    public String getName() {
        return this.name;
    }

    // using setter method
    public void setAge(int a) {
        this.age = a;
    }

    // using getter method
    public int getAge() {
        return this.age;
    }

}